def removing_stopwords(text):
    stop_words = set(stopwords.words("english"))
    stop = [x.lower() for x in stop_words]
    return [word for word in text if not word in stop]


def lemmatization(words_big):
    lemma = WordNetLemmatizer()
    stemmed_words = [lemma.lemmatize(word, "v") for word in words_big]
    stemmed_words = [lemma.lemmatize(word, "n") for word in stemmed_words]
    return " ".join(stemmed_words)


def cleaning(text):
    text = text.lower()
    text = re.sub(r"http\S+|www.\S+", "", text)
    text = remove_contradictions(text)
    text = removing_not(text)
    text = text.split()
    text = removing_shortcuts(text)
    text = " ".join([i for i in text.split() if not i.isdigit()])
    text = word_tokenize(text)
    words_alpha = removing_stopwords(text)
    words_big = [word for word in words_alpha if len(word) > 2]
    clean_text = lemmatization(words_big)
    clean_text = clean_text.replace("   ", " ")
    clean_text = clean_text.replace("  ", " ")
    return clean_text


def get_emotion(input):
    text = cleaning(input).split()
    emotion_values = []
    emotions = {"joy": 0,"anger": 0,"surprise": 0,"sadness": 0,"fear": 0,"anticipation": 0,"disgust": 0,"negative": 0,"positive": 0,"trust": 0,}
    y = 0
    try:
        for word in text:
            if word in df.keys():
                for emotion in df[word]:
                    emotions[emotion] += 1

        if sum(emotions.values()) == 0:
            return None
        for i in emotions:
            emotion_values.append(round((emotions[i] / sum(emotions.values())), 2))
        for j in emotions:
            emotions[j] = emotion_values[y]
            y += 1

        return emotions
    except:
        print("Get_emotion error")


def maxEmotion(emotions):
    if emotions is None:
        return [], []
    else:
        emotions["surprise"] += emotions["anticipation"]
        emotions["anger"] += emotions["disgust"]
        emotions["sadness"] += emotions["negative"]
        emotions["joy"] += emotions["positive"]
        emotions["joy"] += emotions["trust"]
        del emotions["anticipation"]
        del emotions["disgust"]
        del emotions["negative"]
        del emotions["positive"]
        del emotions["trust"]
        max_value = max(emotions.values())
        MainEmotion = [key for key, value in emotions.items() if value == max_value]
        return MainEmotion, emotions
